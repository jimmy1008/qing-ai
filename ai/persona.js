const BIRTHDAY = new Date("2026-02-28T00:00:00+08:00");

function getAgeInDays() {
  const now = new Date();
  const diffMs = now - BIRTHDAY;
  return Math.max(0, Math.floor(diffMs / (1000 * 60 * 60 * 24)));
}

